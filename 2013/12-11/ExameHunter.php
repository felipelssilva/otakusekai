		<p style="text-align: justify;">
			- A prova come&ccedil;ar&aacute; &agrave;s 12h30min e ter&aacute; o t&eacute;rmino at&eacute; 14h00min, faltando dez minutos para o t&eacute;rmino da mesma as equipes ser&atilde;o instru&iacute;das a seguirem para um local, com o One Piece.</p>
		<p style="text-align: justify;">
			- Os envelopes devem ser entregues ao juiz ou ju&iacute;zes do local, para que seja destacado, e entregue o cart&atilde;o. O juiz passara todas as instru&ccedil;&otilde;es da prova e os times devem se reagrupar.</p>
		<p style="text-align: justify;">
			- Haver&atilde;o dois tesouros, o raro e o incomum.</p>
		<p style="text-align: justify;">
			- A prova se resumir&aacute; em conseguir o One Piece, o tesouro mais raro de todos. Existe apenas quatro, e o time que n&atilde;o conseguir pega-lo ser&aacute; eliminado.</p>
		<p style="text-align: justify;">
			- O time que n&atilde;o conseguir um tesouro incomum do seu elemento, perder&aacute; 2 de seus integrantes, sendo que os desclassificados ser&atilde;o escolhidos pelo l&iacute;der.</p>
		<p style="text-align: justify;">
			- Se um time conseguir um tesouro incomum do outro time, ele tem a op&ccedil;&atilde;o de desafiar ou fazer trade. Se ele achar mais de um raro ele &eacute; obrigado a escolher um dos outros times e negociar. O time que tiver 2 tesouros raros no fim da prova ser&aacute; desclassificado.</p>
		<p style="text-align: justify;">
			- O <strong>desafio</strong> &eacute; anunciado ao juiz e os dois times escolhem uma modalidade (caso de desentendimento na escolha o juiz decide na sorte). <strong>Se o vencedor for o time desafiado ele ganha o tesouro raro ou incomum. Se o time que desafiou vence, ganha uma dica e o tesouro raro &eacute; recolocado em outro lugar.</strong></p>
		<p style="text-align: justify;">
			- A op&ccedil;&atilde;o <strong>trade</strong>, tamb&eacute;m &eacute; anunciada ao juiz. Os lideres de cada time negociam os tesouros e dicas como quererem. Qualquer troca de informa&ccedil;&atilde;o al&eacute;m do necess&aacute;rio resultar&aacute; em penalidade.</p>
