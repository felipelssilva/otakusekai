<h1>EXAME HUNTER</h1><p style="text-align: justify;">&nbsp;</p>
		<p style="text-align: justify;">
			<strong>O que &eacute; o Exame Hunter?</strong></p>
		<p style="text-align: justify;">
			O exame Hunter &eacute; a avalia&ccedil;&atilde;o necess&aacute;ria para se adquirir a Licen&ccedil;a Hunter. &Eacute; organizado pela Associa&ccedil;&atilde;o Hunter. Os exames s&atilde;o aplicados pelos examinadores e acontece uma vez por ano.</p>
		<p style="text-align: justify;">
			Consiste em testes que exigem n&atilde;o s&oacute; for&ccedil;a f&iacute;sica ou experi&ecirc;ncia em combate, mas tamb&eacute;m intelig&ecirc;ncia e equil&iacute;brio mental.</p>
		<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;">
			<strong>O que &eacute; o protocolo &ldquo;GREED ISLAND&rdquo;?</strong></p>
		<p style="text-align: justify;">
			Greed Island &eacute; um jogo com nen, para Hunters. Esse ano o Exame Hunter acontecer&aacute; dentro do Greed Island e o protocolo &ldquo;GREED ISLAND&rdquo; foi iniciado pelo pr&oacute;prio presidente da associa&ccedil;&atilde;o para recriar o banco de dados do jogo em no campo do exame e algumas modifica&ccedil;&otilde;es para que qualquer um tivesse acesso sem a necessidade de um anel e um memory card. E o cen&aacute;rio para o exame este ano ser&aacute; a C.A.O.S.!</p>
		<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;">
			<strong>C</strong><strong>omo vai funcionar?</strong></p>
		<p style="text-align: justify;">
			A prova ter&aacute; duas etapas: No <strong>dia 16</strong>, o primeiro dia de prova, os candidatos a Hunter devem <strong>adquirir as 10 cartas de slot espec&iacute;fico. </strong>Para conseguir as cartas, voc&ecirc; deve ganhar os campeonatos, vencer os desafios de cada sala, realizar quests e eventos especiais que aconteceram com tempo determinado.<br />
			No <strong>dia 17</strong>, segundo dia de prova, os candidatos que passaram pra segunda fase passaram por uma <strong>gincana</strong>: Teste, Ca&ccedil;a ao One Piece, Animequiz, Desafio dos Games e por &uacute;ltimo a Queimada! Resultado oficial no palco.</p>
