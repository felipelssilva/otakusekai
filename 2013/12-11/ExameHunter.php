			- Classic<br />
			- Card Game (Como crit&eacute;rio de desempate)</strong></p>
		<p style="text-align: justify;">
			- Todos os times se enfrentaram em 3 modalidades.</p>
		<p style="text-align: justify;">
			- Dentro dessa modalidade o juiz quem escolhe o jogo para as equipes.</p>
		<p style="text-align: justify;">
			- A equipe com mais derrotas &eacute; eliminada</p>
		<p style="text-align: justify;">
			- Se houver empate na quantidade de menos pontos, um game &eacute; feito entre eles pra decidir.</p>
