</p>
		<p style="text-align: justify;">
			<strong>17:30 &ndash; Queimada</strong></p>
		<p style="text-align: justify;">
			<strong>- Prepara&ccedil;&atilde;o</strong></p>
		<p style="text-align: justify;">
			Cada time se coloca num campo, sendo que apenas um jogador de cada lado dever&aacute; se colocar atr&aacute;s da linha de fundo do campo advers&aacute;rio sendo denominado de &quot;cruzar&quot;, que n&atilde;o pode &quot;queimar&quot; enquanto esta nesta fun&ccedil;&atilde;o.</p>
		<p style="text-align: justify;">
			Para decidir sobre a posse da bola e do campo, no in&iacute;cio do jogo, esses dois jogadores vir&atilde;o colocar-se ao centro, entre os dois campos. Ocorre uma disputa de bola . Feito isso, voltam os jogadores aos seus lugares, entregando-se a bola para o cruzar do time que a obteve, para come&ccedil;ar a partida, que &eacute; iniciada ao apito do instrutor.</p>
		<p style="text-align: justify;">
			<strong>- Objetivo</strong></p>
		<p style="text-align: justify;">
			O objetivo visado &eacute; fazer o maior n&uacute;mero poss&iacute;vel de prisioneiros em cada campo. Ser&aacute; vencedor, o grupo que, no fim de um tempo previamente determinado, fizer maior n&uacute;mero de prisioneiros, ou ent&atilde;o, aquele que aprisionar todos os jogadores advers&aacute;rios.</p>
		<p style="text-align: justify;">
			<strong>- Desenvolvimento</strong></p>
		<p style="text-align: justify;">
			Ao ser dado o sinal de in&iacute;cio, um jogador do partido a quem coube a bola, tente entregar a bola ao seu time que tem o objetivo de atira-la ao campo contr&aacute;rio com o prop&oacute;sito de atingir (&quot;queimar&quot;) algum advers&aacute;rio com a bola.</p>
		<p style="text-align: justify;">
			Se o conseguir sem que a bola seja agarrada antes de tocar no ch&atilde;o pelo jogador tocado ou por um companheiro do time dele, o jogador atingido &eacute; considerado prisioneiro e deve sair do seu campo, colocando-se no &quot;cemit&eacute;rio&quot;, espa&ccedil;o posterior &agrave; linha de fundo.</p>
		<p style="text-align: justify;">
			Feito o primeiro prisioneiro, o reserva volta ao seu campo para que tenha oportunidade de jogar tamb&eacute;m nesta posi&ccedil;&atilde;o. Aten&ccedil;&atilde;o: quando o prisioneiro fica pela primeira vez com a bola, n&atilde;o pode &quot;queimar&quot; nenhum componente do time advers&aacute;rio.</p>
		<p style="text-align: justify;">
			A bola que, depois de haver tocado em um jogador, rola ou salta pelo terreno, pode ser recolhida por qualquer jogador, para ser arremessada novamente contra o grupo advers&aacute;rio. A bola pode, tamb&eacute;m ser recolhida por um advers&aacute;rio prisioneiro, a quem, neste caso, se permite apanh&aacute;-la e atir&aacute;-la a um companheiro seu ou queimar o advers&aacute;rio.</p>
		<p style="text-align: justify;">
			O mais recomend&aacute;vel &eacute; agarrar a bola quando arremessada, o jogador s&oacute; &eacute; carimbado se a bola bater nele e cair no ch&atilde;o, do contr&aacute;rio, se o jogador for carimbado, a bola continuar no ar, e ele a agarrar, n&atilde;o ser&aacute; carimbado.</p>
