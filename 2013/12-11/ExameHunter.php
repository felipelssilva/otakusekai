			- Cada equipe enfrentar&aacute; outra equipe em uma rodada de perguntas, podendo conter questionamentos com ou sem alternativas, nome do personagem atrav&eacute;s de foto ou qualquer tipo de pergunta em rela&ccedil;&atilde;o ao anime.</p>
		<p style="text-align: justify;">
			- Ser&aacute; um su&iacute;&ccedil;o de 6 rodadas, onde todos os times dever&atilde;o se enfrentar.</p>
		<p style="text-align: justify;">
			- Cada rodada conter&aacute; dois turnos, sendo que no primeiro turno ser&aacute; exibida uma lista contendo tr&ecirc;s animes aleat&oacute;rios para cada equipe, o qual o trio escolher&aacute; um destes para responder tr&ecirc;s perguntas, no segundo turno ser&aacute; exibido novamente outra lista contendo tr&ecirc;s animes aleat&oacute;rios onde escolher&aacute; o anime para a equipe advers&aacute;ria responder.</p>
		<p style="text-align: justify;">
			- No fim o time com menos pontos ser&aacute; eliminado.</p>
		<p style="text-align: justify;">
			- Se houver empate na quantidade de menos pontos, uma rodada &eacute; feita entre eles pra decidir.</p>
