<strong>4. CATEGORIAS</strong></p>
<ul>
  <li>O concurso &eacute; dividido em duas categorias: (Cosplay Desfile/ Cosplay Livre).<br />
    </li>
  <li>A apresenta&ccedil;&atilde;o ser&aacute; livre.</li>
</ul>
<p><em>COSPLAY DESFILE</em> = Nesta modalidade os participantes dever&atilde;o desfilar no palco onde poder&atilde;o utilizar de falas ou posi&ccedil;&otilde;es dos personagens representados.</p>
<p><span style="color:red;">(O tempo Maximo permitido ser&aacute; de 30seg por apresenta&ccedil;&atilde;o.)</span></p>
<p><em>COSPLAY LIVRE</em> = Aqui os participantes dever&atilde;o fazer uma apresenta&ccedil;&atilde;o onde escolher&atilde;o entre participar individualmente ou em duplas, ou mesmo grupo.</p>
<p><span style="color:red;">(O tempo Maximo permitido ser&aacute; de 5min por apresenta&ccedil;&atilde;o.)</span></p>

<p>5. REGRAS e APRESENTA&Ccedil;√ÉO</p>
<ul>
  <li>As apresenta&ccedil;&otilde;es ser&atilde;o em hor&aacute;rio definidos pela organiza&ccedil;&atilde;o do Evento OTAKU SEKAI.</li>
  <li>Os COSPLAYERS dever&atilde;o comparecer preparados no local de apresenta&ccedil;&atilde;o com TRINTA MINUTOS de anteced&ecirc;ncia do hor&aacute;rio estabelecido para as apresenta&ccedil;&otilde;es, pois ter&atilde;o que ser avaliados no quesito FANTASIA antes da subida ao palco.</li>
  <li>Os COSPLAYERS dever&atilde;o ficar atentos a ordem de chamada para as apresenta&ccedil;&otilde;es, onde n&atilde;o respeitando esta regra estar&aacute; sujeito a penalidade ou desclassifica&ccedil;&atilde;o.</li>
  <li>Durante sua apresenta&ccedil;&atilde;o o COSPLAYER &eacute; inteiramente respons&aacute;vel pelo que acontecer no palco durante sua apresenta&ccedil;&atilde;o.</li>
  <li>Quaisquer danos ocasionados no equipamento ou em qualquer equipamento sendo pr&oacute;prio ou do evento s&atilde;o de responsabilidade sua. Salvo em determinadas ocasi&otilde;es.</li>
  <li> &Eacute; permitido ao COSPLAYER dirigir a palavra aos espectadores como um todo, desde que isso n&atilde;o seja em linguajar ofensivo ou que contem palavras de baixo escal&atilde;o. &Eacute; proibido tamb&eacute;m falar mal, caluniar, insultar ou ca&ccedil;oar qualquer pessoa, entidade, empresa ou clube durante a apresenta&ccedil;&atilde;o.</li>
  <li>E proibido atirar objetos ou qualquer material para ou contra os espectadores e/ou ju&iacute;zes. O COSPLAYER poder&aacute; usar esses recursos limitando-se ao espa&ccedil;o do palco.</li>
  <li>E permitida &agrave; montagem de cen&aacute;rios para apresenta&ccedil;&atilde;o da modalidade LIVRE, porem o competidor ter&aacute; o limite de 2min para montagem.</li>
  <li> &Eacute; permitida a utiliza&ccedil;&atilde;o de replicas de armas de madeira, papel, resina, pl&aacute;stico. N&atilde;o e permitida o uso de replicas de armas de metais e vidro.</li>
  <li> &Eacute; proibida a utiliza&ccedil;&atilde;o de materiais inflam&aacute;veis, pontiagudos, de corte, ou qualquer material que coloque em risco a integridade f&iacute;sica do usu&aacute;rio. Todo material utilizado na apresenta&ccedil;&atilde;o devera ser averiguado pelo staff respons&aacute;vel antes das apresenta&ccedil;&otilde;es.</li>
  <li>E proibida &agrave; divulga&ccedil;&atilde;o de sites, empresas e produtos. </li>
</ul>

