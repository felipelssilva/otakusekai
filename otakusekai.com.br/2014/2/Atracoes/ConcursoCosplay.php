<p><strong>7. JULGAMENTO - CRIT&Eacute;RIO DE AVALIA&Ccedil;&Atilde;O</strong></p>
<ul>
  <li>Os COSPLAYERS e suas fantasias ser&atilde;o julgados por um grupo de CINCO Ju&iacute;zes, escolhidos pela organiza&ccedil;&atilde;o do CAMPEONATO DE COSPLAY. 30 minutos antes das apresenta&ccedil;&otilde;es no palco ser&aacute; iniciada uma avalia&ccedil;&atilde;o no quesito FANTASIA por TR&Ecirc;S ju&iacute;zes, ap&oacute;s isso ser&atilde;o iniciadas as apresenta&ccedil;&otilde;es no palco.</li>
  <li>As notas dadas pelos CINCO ju&iacute;zes ter&atilde;o valores entre 0 e 10 com parciais de 0,5 pontos.</li>
  <li>As notas dos CINCO ju&iacute;zes ser&atilde;o somadas e o resultado ser&aacute; a nota final do competidor.</li>
</ul>
<p>Os competidores do COSPLAY LIVRE ser&atilde;o avaliados nos seguintes quesitos:</p>
<ul>
  <li>DESEMPENHO: Interpreta&ccedil;&atilde;o, qualidade e impacto da apresenta&ccedil;&atilde;o desenvolvida no palco, sincronia de movimento e harmonia entre os participantes.</li>
  <li>COSPLAY: O acabamento e se &eacute; manufaturada e/ou customizada, similaridade aos trajes originais do personagem, riqueza de detalhes de acess&oacute;rios e detalhes.</li>
  <li> FIDELIDADE: Fidelidade do COSPLAYER ao personagem, na interpreta&ccedil;&atilde;o, roupa, f&iacute;sico e desempenho.</li>
</ul>
<p>Os competidores do DESFILE ser&atilde;o avaliados nos seguintes quesitos:</p>
<ul>
  <li> DESEMPENHO: Interpreta&ccedil;&atilde;o.</li>
  <li>COSPLAY: O acabamento, similaridade aos trajes originais do personagem utilizando de materiais recicl&aacute;veis e ou baratos, riqueza de detalhes de acess&oacute;rios e detalhes.</li>
  <li>FIDELIDADE: Fidelidade do COSPLAYER ao personagem, na interpreta&ccedil;&atilde;o, roupa, f&iacute;sico e desempenho.</li>
</ul>

