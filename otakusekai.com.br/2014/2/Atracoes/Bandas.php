<p>A banda mineira Kyoodai Land surgiu em 2013 e tem como proposta divulgar seu trabalho baseado na cultura oriental para o p&uacute;blico.</p>

<p>A atual forma&ccedil;&atilde;o da banda &eacute; Jennifer Pansani (voz), Jefferson Silva (bateria), Matheus Estev&atilde;o (guitarra solo), Alexandre Wallan (guitarra base), J&eacute;ssica Ara&uacute;jo (Teclado) e Lucas Neves (contrabaixo). </p>

<p>Possui um amplo repert&oacute;rio, fazendo covers de bandas proeminentes como Flow, Scandal, Fade e Orange Range e tamb&eacute;m inclui m&uacute;sicas como GO!, Shunkan Sentimental, One Reason e Asterisk. </p>

<p>Al&eacute;m disso tamb&eacute;m tocamos Anime Songs em suas vers&otilde;es originais e traduzidas, como as famosas m&uacute;sicas Chala Head Chala e Sorriso Resplandecente.</p>

