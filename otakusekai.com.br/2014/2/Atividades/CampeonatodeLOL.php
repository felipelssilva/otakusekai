<p><strong>DESCONEX&Otilde;ES</strong></p>

<p>- Procedimentos de parada de jogo / Regras de Pause:</p>

<p>1) Se uma desconex&atilde;o involunt&aacute;ria ocorrer durante qualquer per&iacute;odo da partida, a equipe pode utilizar-se do sistema de parada do jogo pelo prazo de 2 minutos para que o membro reconecte.<br />
2) Se um jogador intencionalmente desconectar-se sem especificar a raz&atilde;o, ent&atilde;o o jogo continuar&aacute; normalmente.<br />
3) Se ocorrer uma falha do servidor, em seguida, o &aacute;rbitro principal decidir&aacute; ou entre (a) o rein&iacute;cio do jogo ou (b) a atribui&ccedil;&atilde;o do jogo para uma das equipes.<br />
