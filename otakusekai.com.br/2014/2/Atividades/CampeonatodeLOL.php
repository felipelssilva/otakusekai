<p>1) Pausa (/pause) pode ser solicitada por um administrador do torneio ou por um jogador, com base nas seguintes condi&ccedil;&otilde;es </p>

<p>a) Ser&atilde;o permitidos 20 minutos de PAUSE na partida, sendo o m&aacute;ximo de 5 minutos para cada equipe, totalizando um total de 10 minutos para as equipes, o tempo restante poder&aacute; ser utilizado pela organiza&ccedil;&atilde;o, conforme as circunst&acirc;ncias.<br />
