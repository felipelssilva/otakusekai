		<p style="text-align: justify;">
			<strong>YU-GI-OH!</strong></p>
<p style="text-align: justify;"><strong>Deck</strong></p>
		<ul>
			<li style="margin-left: 40px; text-align: justify;">
		  O Deck usado deve ter entre 40 a 60 cartas.(s&oacute;mente ser&atilde;o aceitas cartas originais)</li>
			<li style="margin-left: 40px; text-align: justify;">
				Este Deck &eacute; chamado de Main Deck (Deck Principal).</li>
			<li style="margin-left: 40px; text-align: justify;">
				Voc&ecirc; somente pode ter at&eacute; 3 c&oacute;pias de uma mesma carta no seu Deck, Extra Deck e Side Deck combinadamente(a menos que estas sejam , proibidas, limitadas ou semi-limitadas)</li>
</ul>
		<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;">
			<strong>Extra Deck</strong></p>
		<p style="text-align: justify;">
			Este Deck &eacute; constitu&iacute;do por monstros Synchro , Fusion e Xyz, que podem ser usados durante o jogo se voc&ecirc; seguir certas condi&ccedil;&otilde;es. As regras para o Extra Deck s&atilde;o:<br />
			&nbsp;</p>
		<ul style="margin-left: 40px;">
			<li style="text-align: justify;">
				Limite de at&eacute; 15 cartas no Extra Deck.</li>
			<li style="text-align: justify;">
				O Extra Deck somente pode conter monstros Synchro, Xyz e Fusion monsters.</li>
			<li style="text-align: justify;">
				Essas cartas n&atilde;o s&atilde;o inclusas na contagem m&iacute;nima e m&aacute;xima de cartas do seu Main Deck.</li>
		</ul>
		<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;"><strong>
		  Side Deck (MUNDAN&Ccedil;A)</strong></p>
		<p style="text-align: justify;">
			Este &eacute; um Deck separado de cartas que voc&ecirc; pode usar para mudar seu Deck durante uma Partida. Depois de cada Duelo numa Partida, voc&ecirc; pode trocar qualquer carta do seu Side Deck por uma carta do seu Main Deck para customizar a sua estrat&eacute;gia contra o seu oponente. O n&uacute;mero de cartas no seu Side Deck n&atilde;o deve exceder o limite de 15 cartas, e deve ser exatamente igual ao n&uacute;mero de cartas que haviam nele no in&iacute;cio da Partida.</p>
		<p style="text-align: justify;">&nbsp;</p>
		<p style="text-align: justify;"><strong>
		  Cartas proibidas, limitadas e semi-limitadas.</strong></p>
		<p style="text-align: justify;">
			&Eacute; uma lista de cartas feita pela Konami que regula as cartas que participam de campeonatos oficias e ser&aacute; seguida neste campeonato. Cartas na lista de proibidas n&atilde;o podem ser usadas. Cartas na lista de limitadas podem ser usadas, por&eacute;m apenas uma copia dela ser&aacute; permitida. Cartas da lista de Semi-limitadas poder&atilde;o ser usadas em at&eacute; duas copias.<br />
			Lista no link a seguir:</p>
