<p style="text-align: justify;">
			<span style="text-align: justify;">Mostre o guerreiro dentro de voc&ecirc;! Aqui simula-se as arenas de batalhas no estilo antigo e medieval, ou seja, lutas com espadas e outras armas brancas.</span></p>
		<p style="text-align: justify;">
			Estas &quot;armas&quot; s&atilde;o meramente cenogr&aacute;ficas, feitas em materiais emborrachados ou revestidos de espuma, e a pr&aacute;tica &eacute; muito divertida, visto que a possibilidade de machucados e les&otilde;es de qualquer natureza s&atilde;o m&iacute;nimos e por qualquer pessoa pode participar.</p>
		<p style="text-align: justify;">
			O freeplay acontece os dois dias todos, com exce&ccedil;&atilde;o do momento dos campeonatos. O valor &eacute; R$2,50, melhor de 3 e cada round &eacute; 100 pontos ou 3 minutos.</p>
		<p style="text-align: justify;">
			<strong>Regras:&nbsp;</strong></p>
		<p style="text-align: justify;">
			<strong>1. Informa&ccedil;&otilde;es Gerais</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			1.1 &ndash; A Batalha Campal no modo competitivo ocorrer&aacute; no dias 17 de novembro.<br />
			1.2 &ndash; A Participa&ccedil;&atilde;o nesta modalidade ser&aacute; ind&iacute;dual.<br />
			1.3 &ndash; A competi&ccedil;&atilde;o se dar&aacute; em um n&uacute;mero prop&iacute;cio para as chaves.<br />
			1.4 &ndash; As inscri&ccedil;&otilde;es da Batalha Campal come&ccedil;am &agrave;s 10h00.<br />
			1.5 - Os candidatos que se inscreverem ap&oacute;s preenchimento de todas as vagas que fechem a chave, ficar&atilde;o em lista de espera, caso algum outro n&atilde;o compare&ccedil;a no momento ou aumentem as chaves.<br />
			1.6 &ndash; No ato da inscri&ccedil;&atilde;o o candidato deve estar presente para preencher as fichas de inscri&ccedil;&atilde;o.<br />
			1.7 &ndash; A inscri&ccedil;&atilde;o da Batalha Campal &eacute; no valor de R$5,00.<br />
			1.8 &ndash; O candidato deve comparecer meia hora antes da competi&ccedil;&atilde;o come&ccedil;ar e estar na hora em que foi chamado para uma partida, aquele que n&atilde;o estiver, ser&aacute; subst&iacute;tuido por algu&eacute;m previamente inscrito ou a vit&oacute;ria ser&aacute; dada ao outro participante.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>2. Batalhas</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			2.1 &ndash; O objetivo da Batalha Campal &eacute; um duelo entre dois oponentes, valendo pontos por cada golpe no corpo do oponente, empunhando uma arma cenogr&aacute;fica. Ao final da batalha, ser&aacute; somada a pontua&ccedil;&atilde;o atingida por cada lutador e ser&aacute; determinado (a) o (a) vencedor pela pontua&ccedil;&atilde;o atingida!<br />
			2.2 - Funcionar&aacute; da seguinte forma, com a espada em m&atilde;os, atingir o oponente APENAS em partes determinadas do corpo, ou seja, bra&ccedil;os, peito, pernas e abd&ocirc;men.<br />
			2.3 - Golpes nos bra&ccedil;os e pernas d&atilde;o ao atacante, 10 pontos; golpes no peito ou abd&ocirc;men d&atilde;o ao atacante 20 pontos. Golpes na cabe&ccedil;a, rosto ou partes &iacute;ntimas e sens&iacute;veis, ir&atilde;o retirar do atacante 20 pontos.<br />
			2.4 - As batalhas ser&atilde;o desenvolvidas em rounds de 3 minutos, e ser&aacute; o vencedror aquele que atingir mais pontos nesse tempo, ou completar 100.<br />
			2.5 - A cada ponto conquistado por qualquer um dos dois &quot;guerreiros&quot;, o juiz da Batalha Campal ir&aacute; paralisar o combate, informar a pontua&ccedil;&atilde;o adquirida, onde foi o golpe e ent&atilde;o, ao comando do Juiz, se reiniciar&aacute; o combate, at&eacute; o t&eacute;rmino do round.<br />
			2.6 - Os vencidos s&atilde;o automaticamente eliminados e o vencedor segue em frente enfrentando os demais vencedores.<br />
			2.7 - O juiz tem o direito de mudar as condi&ccedil;&otilde;es de vit&oacute;ria, se achar necess&aacute;rio.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>3. Penalidades</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			3.1 - Para tornar mais efetivo o cuidado durante os &quot;combates&quot;, golpes na cabe&ccedil;a e partes intimas ser&atilde;o punidos com penalidade de -20 pontos do jogador da arena caso haja intencionalidade.<br />
			3.2 - Fica estipulado que qualquer golpe que invalide o oponente resultar&aacute; em desclassifica&ccedil;&atilde;o imediata.<br />
			3.3 &ndash; Se o Juiz perceber que o participante causou uma falta de prop&oacute;sito, resultar&aacute; em desclassifica&ccedil;&atilde;o imediata.<br />
			3.4 - Se o juiz considerar que o modo de luta de um dos participantes for muito perigoso, ou seja, oferecer risco de machucar o oponente, o juiz pode parar o combate.<br />
			3.5 - O juiz tem TOTAL autoridade sobre as partidas, o desrespeito resultar&aacute; em desclassifica&ccedil;&atilde;o imediata.</p>
