			<strong>DESCONEX&Otilde;ES</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			- Procedimentos de parada de jogo / &nbsp;Regras de Pause:</p>
		<p style="margin-left: 40px; text-align: justify;">
			Se uma desconex&atilde;o involunt&aacute;ria ocorrer durante qualquer per&iacute;odo da partida, a equipe pode utilizar-se do sistema de parada do jogo pelo prazo de 2 minutos para que o membro reconecte.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Se um jogador intencionalmente desconectar-se sem especificar a raz&atilde;o, ent&atilde;o o jogo continuar&aacute; normalmente.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Se ocorrer uma falha do servidor, em seguida, o &aacute;rbitro principal decidir&aacute; ou entre (a) o rein&iacute;cio do jogo ou (b) a atribui&ccedil;&atilde;o do jogo para uma das equipes.</p>
