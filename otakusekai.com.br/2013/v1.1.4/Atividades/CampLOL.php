</p>
		<p style="text-align: justify;">
			<strong>PAUSA</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			Pausa (/pause) pode ser solicitada por um administrador do torneio ou por um jogador, com base nas seguintes condi&ccedil;&otilde;es&nbsp;</p>
		<p style="margin-left: 40px; text-align: justify;">
			Ser&atilde;o permitidos 20 minutos de PAUSE na partida, sendo o m&aacute;ximo de 5 minutos para cada equipe, totalizando um total de 10 minutos para as equipes, o tempo restante poder&aacute; ser utilizado pela organiza&ccedil;&atilde;o, conforme as circunst&acirc;ncias.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Administradores do torneio poder&atilde;o solicitar a pausa de uma partida por qualquer motivo, Administradores do torneio em um torneio ao vivo s&atilde;o autorizados a executar um comando de pausa em qualquer esta&ccedil;&atilde;o de jogador.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Os jogadores podem pausar o jogo se uma das seguintes condi&ccedil;&otilde;es adicionais forem atendidas: -Depois de algum jogador ter sa&iacute;do do jogo devido a qualquer forma de desconex&atilde;o ou falha do computador. A mensagem &ldquo;jogador foi desconectado&rdquo; deve aparecer na tela.</p>
		<p style="margin-left: 40px; text-align: justify;">
			- O jogador poder&aacute; pausar o jogo devido ao mau funcionamento do hardware (monitor, perif&eacute;ricos, etc.).</p>
		<p style="margin-left: 40px; text-align: justify;">
			O jogo n&atilde;o ser&aacute; retomado (/unpause) at&eacute; que a libera&ccedil;&atilde;o de um Administrador seja emitida e todos os jogadores sejam notificados e estejam prontos em suas esta&ccedil;&otilde;es.</p>
		<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;">
			<strong>JOGO DESLEAL</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			As seguintes a&ccedil;&otilde;es ser&atilde;o consideradas como desleais:</p>
		<p style="margin-left: 40px; text-align: justify;">
			O uso de qualquer programa de fraudes (cheats)</p>
		<p style="margin-left: 40px; text-align: justify;">
			Desconex&atilde;o intencional</p>
		<p style="margin-left: 40px; text-align: justify;">
			O uso de qualquer configura&ccedil;&atilde;o diferente das configura&ccedil;&otilde;es padr&atilde;o e permitidas</p>
		<p style="margin-left: 40px; text-align: justify;">
			Qualquer &ldquo;conversa desnecess&aacute;ria&rdquo; durante a partida</p>
		<p style="text-align: justify;">
			*Conversa desnecess&aacute;ria: Qualquer conversa para al&eacute;m dos casos listados a seguir:</p>
		<p style="margin-left: 40px; text-align: justify;">
			Mensagem de sauda&ccedil;&atilde;o entre os jogadores;</p>
		<p style="margin-left: 40px; text-align: justify;">
			Repetir &ldquo;P&rdquo; para solicitar uma pausa durante o jogo;</p>
		<p style="margin-left: 40px; text-align: justify;">
			Um sinal para derrota (exemplos: &ldquo;gg&rdquo; ou qualquer outra mensagem que o &aacute;rbitro possa usar para decidir o vencedor);</p>
		<p style="margin-left: 40px; text-align: justify;">
			Conversa entre os jogadores que n&atilde;o violem a cl&aacute;usula antidesportiva de comportamento;</p>
		<p style="margin-left: 40px; text-align: justify;">
			Qualquer mensagem que o juiz considere como parte do jogo.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Claramente permitir ao advers&aacute;rio ganhar o jogo;</p>
		<p style="margin-left: 40px; text-align: justify;">
			Na arena com cada time poder&aacute; ficar uma pessoa (t&eacute;cnico ou patrocinador do time). Caso o juiz entenda que essa pessoa esteja interferindo no andamento do campeonato ele poder&aacute; retirar tal pessoa e a equipe receber&aacute; um aviso. Caso a situa&ccedil;&atilde;o persista a equipe ser&aacute; punida com derrota.</p>
		<p style="margin-left: 40px; text-align: justify;">
			Comportamento antidesportivo ou, em geral, perturbador (por exemplo, gritando), a&ccedil;&otilde;es inadequadas e/ou ofensivas voltadas para outro jogador (Mesmo dentro do jogo).</p>
		<p style="margin-left: 40px; text-align: justify;">
			O uso de um bug do jogo, que &eacute; determinada pelo conselho de &aacute;rbitros como sendo injusto.</p>
		<p style="margin-left: 40px; text-align: justify;">
			- Caso algum jogador cometa quaisquer viola&ccedil;&atilde;o considerada como jogo desleal, poder&aacute;, a crit&eacute;rio do conselho de &aacute;rbitros, receber um aviso, perder ou, em casos extremos, ser desclassificado do torneio.</p>
		<p style="margin-left: 40px; text-align: justify;">
			- Durante a Grande Final, o conselho de &aacute;rbitros pode determinar outras a&ccedil;&otilde;es para incorporar ao &ldquo;jogo injusto&rdquo;.</p>
