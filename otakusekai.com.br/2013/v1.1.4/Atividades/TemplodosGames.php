</p>
		<p style="text-align: justify;">
			<strong>Super Smash Bross</strong></p>
		<p style="text-align: justify;">
			Console para o campeonato:&nbsp; Nintendo Wii</p>
		<p style="text-align: justify;">
			O modelo utilizado ser&aacute; no estilo mata-mata (elimina&ccedil;&atilde;o direta) no modo &ldquo;Pro Tournament&rdquo;&nbsp; (um contra um)</p>
		<p style="text-align: justify;">
			As lutas ser&atilde;o disputadas dentro de 5 (cinco) minutos com tr&ecirc;s vidas para cada jogador.&nbsp; O melhor da disputa ser&aacute; declarado vencedor</p>
		<p style="text-align: justify;">
			Qualquer personagem poder&aacute; ser utilizado.&nbsp; O ju&iacute;z dever&aacute; ser informado secretamente sobre o personagem a ser escolhido antes da luta</p>
		<p style="text-align: justify;">
			Poder&atilde;o ser usados os mesmos personagens em uma mesma luta</p>
		<p style="text-align: justify;">
			O level ser&aacute; escolhido aleatoriamente</p>
		<p style="text-align: justify;">
			O Damage Ratio ser&aacute; de 1.0</p>
		<p style="text-align: justify;">
			O uso de Itens ser&aacute; desabilitado e n&atilde;o haver&aacute; Handicap</p>
		<p style="text-align: justify;">
			O oponente de cada jogador ser&aacute; selecionado segundo a lista de inscri&ccedil;&otilde;es, ou seja, o inscrito n&uacute;mero 1 lutar&aacute; com o inscrito n&uacute;mero 2 e assim por diante</p>
		<p style="text-align: justify;">
			A partida final do campeonato ser&aacute; organizada em 3 (tr&ecirc;s) lutas com cinco vidas para cada jogador e sem limite de tempo.&nbsp; Nela, s&oacute; ser&aacute; permitida a troca de personagem ao jogador vencido na partida</p>
		<p style="text-align: justify;">
			Todos os jogadores participantes dever&atilde;o estar na arena (sala) em at&eacute; 15 (quinze) minutos antes do come&ccedil;o do torneio.&nbsp; Uma chamada ser&aacute; feita nesse tempo para confirmar a presen&ccedil;a de cada participante.&nbsp; O jogador que n&atilde;o estiver presente ser&aacute; eliminado do campeonato por W.O.</p>
		<p style="text-align: justify;">
			Antes de cada luta, ser&aacute; anunciado o nome dos jogadores da vez apenas 3 (tr&ecirc;s) vezes.&nbsp; Caso um deles n&atilde;o estiver presente, ser&aacute; eliminado do campeonato por W.O.</p>
		<p style="text-align: justify;">
			As lutas dever&atilde;o ocorrer de forma fluente, sem interrup&ccedil;&otilde;es.&nbsp; Desta forma, o uso do PAUSE est&aacute; PROIBIDO.&nbsp; O jogador que descumprir essa regra estar&aacute; eliminado.</p>
		<p style="text-align: justify;">
			O jogador que atrapalhar o oponente com qualquer tipo de contato f&iacute;sico ser&aacute; eliminado da competi&ccedil;&atilde;o.</p>
		<p style="text-align: justify;">
			Qualquer tipo de ofensa, mesmo verbal, que o ju&iacute;z achar inaceit&aacute;vel ir&aacute; eliminar o jogador da competi&ccedil;&atilde;o.</p>
		<p style="text-align: justify;">
			Cada jogador poder&aacute; configurar o joystick de acordo com suas prefer&ecirc;ncias antes da luta</p>
		<p style="text-align: justify;">
			O jogador est&aacute; livre para trazer o seu pr&oacute;prio joystick para a competi&ccedil;&atilde;o, caso preferir</p>
		<p style="text-align: justify;">
			As inscri&ccedil;&otilde;es ser&atilde;o feitas na sala Super Smash Bros Brawl no dia do evento das 8h &agrave;s 13h</p>
