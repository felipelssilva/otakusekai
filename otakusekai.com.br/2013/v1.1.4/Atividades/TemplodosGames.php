<p style="text-align: justify;">Prepare-se, pois nessa sala voc&ecirc; entrar&aacute; no mundo dos games e n&atilde;o vai querer sair nunca mais! Com a linha do tempo contando a hist&oacute;ria dos videogames e os t&atilde;o aclamados jogos que fizeram parte de nossas inf&acirc;ncias, come&ccedil;ando com uma viagem no tempo que vai at&eacute; os dias de hoje e come&ccedil;am! Campeonatos, quiz e muito mais voc&ecirc; s&oacute; v&ecirc; aqui, no Templo dos Games!</p>
		<p style="text-align: justify;">
			<strong>Regras gerais:</strong></p>
		<p style="text-align: justify;">
			- &Eacute; proibido apertar pause durante o duelo. Se usado, s&oacute; poder&aacute; retornar a batalha se o advers&aacute;rio concordar. Se n&atilde;o, ser&aacute; desclassificado.<br />
			- Extremamente recomend&aacute;vel que leve seu PR&Oacute;PRIO CONTROLE para os campeonatos. N&atilde;o ser&atilde;o admitidas reclama&ccedil;&otilde;es sobre os controles fornecidos para uso no local.<br />
			&ndash; Quaisquer danos ao controle ou qualquer outro aparelho ser&aacute; pago por quem estragou.<br />
			- N&atilde;o ofenda ou grite com seu advers&aacute;rio, uma vez que ele se sinta incomodado com isso.<br />
			&ndash; Se a plat&eacute;ia incomodar algum dos jogadores, eles tem o direito de pedir sil&ecirc;ncio para se concentrar na partida.<br />
			- O Hor&aacute;rio &eacute; extremamente importante, com 30 minutos de toler&acirc;ncia. Caso exceda esse tempo, ser&aacute; desclassificado e sem o direito a reembolso.<br />
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>3 - Regras padr&otilde;es dos jogos de luta</strong></p>
		<p style="text-align: justify;">
			- &Eacute; proibido escolher duas vezes o mesmo personagem. S&oacute; ser&aacute; v&aacute;lido escolhe-lo ap&oacute;s jogar com outro.<br />
			- Sistema de batalhas solo. Sem utiliza&ccedil;&atilde;o de tag.<br />
			- Est&aacute;gios ser&atilde;o escolhidos a partir de AMBOS os players de acordo. Caso n&atilde;o ser&aacute; selecionado RANDOM.<br />
			- &Eacute; permitido usar o mesmo golpe quantas vezes desejar.</p>
