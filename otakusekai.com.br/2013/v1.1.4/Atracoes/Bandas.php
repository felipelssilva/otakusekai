<p><strong>SURGE~outofdesperation</strong></p>
<p><img src="Fotos/surge.png" alt="" width="750" /></p>

<p>SURGE~outofdesperation &eacute; uma banda de Visual Rock, com uma est&eacute;tica fortemente influenciada por bandas dos segmentos Visual Kei, Angura Kei e Nagoya Kei (estilos de rock japon&ecirc;s).</p>

<p>Formada por cinco membros, a banda SURGE~ (como &eacute; chamada abreviadamente) iniciou seus trabalhos no in&iacute;cio de 2010 preparando-se em est&uacute;dios para apresentar um som de alta qualidade. No final de 2010 a banda iniciou a realiza&ccedil;&atilde;o de shows em Belo Horizonte (seu local de origem) em eventos relacionados &agrave; cultura japonesa.</p>

<p>Depois da realiza&ccedil;&atilde;o de um show no Anime BH 2011 em Belo Horizonte, a banda SURGE~ chamou a aten&ccedil;&atilde;o pela qualidade musical e visual no palco, despertando o interesse de um selo japon&ecirc;s para a grava&ccedil;&atilde;o de um &aacute;lbum.</p>

<p>O &aacute;lbum foi gravado em Belo Horizonte e produzido por Robert Regonati, renomado produtor musical que trabalha com nomes importantes da m&uacute;sica japonesa, como a cantora Tsubasa Imamura, e mixado e masterizado por ele e por Takeo Senda no Nakanotouge Music Labo (Jap&atilde;o).</p>

<p>O &aacute;lbum ainda n&atilde;o foi lan&ccedil;ado, por&eacute;m &eacute; poss&iacute;vel verificar a qualidade da produ&ccedil;&atilde;o atrav&eacute;s do Digital Single バイオレット (Baioretto) e do Digital Maxi-Single 絶望の色 (Zetsubou no iro), lan&ccedil;ados no site www.surgeout.com .</p>
<p><br />
<strong>SURGE~outofdesperation</strong> apresenta letras voltadas para cr&iacute;ticas &agrave; banaliza&ccedil;&atilde;o dos sentimentos, religi&atilde;o, conflitos sentimentais e tabus sociais tratados de forma po&eacute;tica.</p>

<p>O grande diferencial da banda s&atilde;o os visuais elaborados e pouco comuns no Brasil, al&eacute;m do grande cuidado com o conte&uacute;do musical e significados. Cuidado esse que se estende at&eacute; a escolha da grafia do nome da banda para exprimir a ideologia da banda entre s&iacute;mbolos e uso de mai&uacute;sculas e min&uacute;sculas associados ao significado da palavra. Al&eacute;m disso, a complexidade da l&iacute;ngua japonesa somada ao rock sempre gera admira&ccedil;&atilde;o e curiosidade do p&uacute;blico que nunca teve acesso a algum material sobre o estilo.</p>

<p>O destaque e a originalidade da banda SURGE~outofdesperation renderam uma mat&eacute;ria sobre a mesma na revista japonesa ZOUSS n&ordm; 88 e cita&ccedil;&otilde;es a respeito da banda no programa sobre m&uacute;sica brasileira da r&aacute;dio japonesa 76,3. Al&eacute;m disso a m&uacute;sica Massive Multiplayer OnLife (MMO) foi utilizada em um comercial web do “Live Karaoke in Hokuriku ” em Ishikawa, Jap&atilde;o, e a banda cedeu uma entrevista ao site franc&ecirc;s Kochipan (www.kochipan.org) e uma ao site norte-americano Visual Keios (<a href="http://www.visualkeios.com/2012/10/surge-interview/" target="_blank">http://www.visualkeios.com/2012/10/surge-interview/</a>).</p>

<p><a href="http://www.surgeout.net/" target="_blank">http://www.surgeout.net/</a></p>
