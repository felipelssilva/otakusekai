<p><strong>B-Quatro </strong></p>
<p><img src="Fotos/b-quatro.jpg" alt="" width="744" height="232" /></p>

<p>Legitimamente uberabense a B-quatro integra a tradi&ccedil;&atilde;o musical mineira e vem se destacando com o seu pop-rock de qualidade e estilo pr&oacute;prio.</p>
<p>Composta por Marco G&oacute;mez (Vocal), Marcelo Vieira (Guitarra), Rog&eacute;rio (Teclados), Chaene da Gama (Baixo) e Rodrigo Augusto (Bateria), vem impriminado a sua marca no cen&aacute;rio sudeste desde de 2000.</p>
<p>Durante estes 13 anos de exist&ecirc;ncia, J&aacute; dividiu palco com grandes nomes da m&uacute;sica popular brasileira, tais como Capital Inicial, Tihuana, Mr Gym e Wilson Sideral, sendo tamb&eacute;m destaque no Concurso de Bandas &quot;Tri&acirc;ngulo Music&quot;, um dos maiores festivais de bandas do Brasil, evento realizado pela rede Integra&ccedil;&atilde;o de TV e CTBC, conseguindo se figurar entre as tr&ecirc;s melhores bandas independentes de toda Minas Gerais, destaque tambem no concurso de bandas &quot;Jo&atilde;o Rock 2013&quot; um concurso com 530 bandas inscritas do Brasil inteiro se figurando entre as 3 mais votadas.</p>
<p>Acostumada com grandes eventos, J&aacute; fez show pra mais de 15 mil pessoas, al&eacute;m de ser bastante conhecia do p&uacute;blico Uberabense, sempre tem presen&ccedil;a marcada em todas as grandes casas de shows da cidade e da regi&atilde;o.</p>

<p><a href="http://www.bquatro.com/" target="_blank">http://www.bquatro.com/</a></p>
