<p>Chegou &agrave; hora de testar seus conhecimentos sobre os Anime, Mang&aacute;, Tokusatsu, Comics entre outros! Venha para o palco encarar esse super desafios e ganhar pr&ecirc;mios! </p>
 
<p>&nbsp;</p>

<p style="text-align: justify;">
			<strong>1. Informa&ccedil;&otilde;es Gerais</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			1.1 &ndash; O Anime Quiz ocorrer&aacute; &agrave;s __ horas e __ minutos.<br />
			1.2 &ndash; A Participa&ccedil;&atilde;o nesta modalidade de Quiz ser&aacute; realizada em trios.<br />
			1.3 &ndash; A competi&ccedil;&atilde;o se dar&aacute; entre, no m&aacute;ximo, 8 (oito) equipes inscritas.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>2. Inscri&ccedil;&atilde;o</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			2.1 &ndash; As inscri&ccedil;&otilde;es do Anime Quiz come&ccedil;am &agrave;s 10h00 do Sabado e ser&atilde;o finalizadas com meia hora de anteced&ecirc;ncia do concurso no palco.<br />
			2.2 &ndash; Ser&atilde;o apenas 08 (oito) vagas para as equipes. Os trios que se inscreverem ap&oacute;s preenchimento de todas as vagas, ficar&atilde;o em lista de espera, caso o grupo n&atilde;o compare&ccedil;a no momento do campeonato.<br />
			2.3 &ndash; A equipe tem o direito de inscrever 1 (um) substituto, que s&oacute; participar&aacute; do campeonato na falta de algum membro. O substituto n&atilde;o pode ser modificado ap&oacute;s a inscri&ccedil;&atilde;o, e n&atilde;o poder&aacute; estar inscrito em nenhum outro grupo.<br />
			2.4 &ndash; No ato da inscri&ccedil;&atilde;o o trio deve estar presente para preencher as fichas de inscri&ccedil;&atilde;o.<br />
			2.5 &ndash; Cada equipe deve escolher um nome, que n&atilde;o pode ser ofensivo, nem conter palavr&otilde;es ou palavras inapropriadas.<br />
			2.6 &ndash; A inscri&ccedil;&atilde;o do Anime Quiz &eacute; gratuita.<br />
			2.7 &ndash; A equipe deve ser composta por no m&aacute;ximo tr&ecirc;s componentes, contudo, &eacute; poss&iacute;vel se inscrever no Anime Quis em dupla ou individualmente.</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>3. Da Prepara&ccedil;&atilde;o</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			3.1 &ndash; Os trios devem comparecer na sala do evento com meia hora de anteced&ecirc;ncia do concurso, para as &uacute;ltimas instru&ccedil;&otilde;es. O atraso acarretar&aacute; em desclassifica&ccedil;&atilde;o.<br />
			3.2 &ndash; &Eacute; necess&aacute;rio que a equipe esteja presente, caso um n&atilde;o esteja presente, pode-se solicitar o substituto que foi previamente inscrito. Caso o substituto tamb&eacute;m n&atilde;o esteja presente, o participante concorrer&aacute; sozinho no Quiz.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>4. Da Rodada de Perguntas</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			4.1 &ndash; Cada equipe enfrentar&aacute; outra equipe em uma rodada de perguntas, podendo conter questionamentos com ou sem alternativas, nome do personagem atrav&eacute;s de foto ou qualquer tipo de pergunta em rela&ccedil;&atilde;o ao anime.<br />
			4.2 &ndash; Cada rodada conter&aacute; at&eacute; tr&ecirc;s turnos, sendo que no primeiro turno ser&aacute; exibida uma lista contendo tr&ecirc;s animes aleat&oacute;rios para cada equipe, o qual o trio escolher&aacute; um destes para responder tr&ecirc;s perguntas, no segundo turno ser&aacute; exibido novamente outra lista contendo tr&ecirc;s animes aleat&oacute;rios onde escolheram o anime para a equipe advers&aacute;ria responder. Caso haja empate, no terceiro turno, o julgador far&aacute; perguntas aleat&oacute;rias de qualquer anime para cada equipe, at&eacute; que haja um vencedor.<br />
			4.3 &ndash; As chaves ser&atilde;o formadas aleatoriamente ap&oacute;s a inscri&ccedil;&atilde;o das duplas.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>5. Proibi&ccedil;&otilde;es</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			5.1 &ndash; N&atilde;o &eacute; permitido uso de celular no momento do campeonato, nem de nenhum aparelho eletr&ocirc;nico.<br />
			5.2 &ndash; A equipe que receber ajuda da plat&eacute;ia ser&aacute; penalizada em uma quest&atilde;o anulada para cada ajuda que receber.<br />
			5.3 &ndash; Uma equipe n&atilde;o deve atrapalhar outra, podendo ser penalizada em uma quest&atilde;o anulada por isso.&nbsp;</p>
		<p style="text-align: justify;">
			&nbsp;</p>
		<p style="text-align: justify;">
			<strong>6. Premia&ccedil;&atilde;o</strong></p>
		<p style="margin-left: 40px; text-align: justify;">
			6.1 &ndash; A premia&ccedil;&atilde;o ser&aacute; entregue no momento em que o Quiz for encerrado.<br />
			6.2 &ndash; Resultados oficiais ser&atilde;o publicados nas redes sociais e todos meios de divulga&ccedil;&atilde;o que o evento utiliza.<br />
			6.3 &ndash; O evento reserva-se o direito de publicar fotos e v&iacute;deos da competi&ccedil;&atilde;o em suas publicidades em geral, cartazes, sites, blogs, etc.<br />
			6.4 &ndash; A premia&ccedil;&atilde;o ser&aacute; simb&oacute;lica, sendo brindes aos melhores colocados.<br />
			6.5 &ndash; A organiza&ccedil;&atilde;o reserva-se no direito de realizar qualquer mudan&ccedil;a sem pr&eacute;vio aviso.</p>
