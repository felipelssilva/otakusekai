<h1><strong>CARAVANAS</strong><br>
</h1>
<p><strong>COMO MONTAR?</strong></p>
<p>Informe previamente as informações de sua caravana preenchendo o formulário nos enviando por e-mail. Contrate uma empresa de ônibus ou van, de acordo com a quantidade de pessoas, horário e a forma de pagamento. Inclua também o valor dos ingressos. </p>
<p><strong>INGRESSOS</strong></p>
<p>Os integrantes das caravanas dentro do raio de cerca de 300km da cidade de Uberaba pagam o mesmo valor do ingresso Antecipado. Integrantes das caravanas de fora dessa área, pagam apenas R$10,00, como mostra no gráfico a seguir:</p>
<p><img src="Fotos/Grafico_das_caravanas.jpg" width="720" height="569"></p>
<p>Todo organizador tem a entrada vip e a cada 10 pessoas, você ganha mais uma cortesia.<br>
  10 pessoas pagantes = 2 Cortesias = organizador + 1 amigo (Total de 12 pessoas)<br>
  20 pessoas pagantes = 3 Cortesias = organizador + 2 amigos (Total de 23 pessoas)<br>
  <strong>30 pessoas pagantes = 5 Cortesias = organizador + 4 amigos (Total de 35 pessoas)</strong><br>
</p>
<p><strong>REGRAS</strong></p>
<p>- O organizador deve possuir mais de 18 anos;<br>
  - O organizador deve preencher corretamente o formulário de cadastro de caravana;<br>
  - Haverá um guichê exclusivo para as caravanas. O pagamento deverá ser efetuado no mesmo.<br>
  - O organizador, e tão somente ele, deverá se dirigir até o guichê e informar sua chegada no evento;<br>
  - As caravanas serão atendidas por ordem de chegada no evento.<br>
  - Para ser considerada caravana, ela deve possuir, pelo menos, 10 pessoas pagantes, não contando com o organizador. <br>
  - Os casos omissos no regulamento serão analisados pela coordenação do evento. A decisão será soberana e inquestionável, não cabendo interferência do organizador da caravana. </p>
