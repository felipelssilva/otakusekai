2.</strong> Inscri&ccedil;&atilde;o
<div style="padding-left:20px;"><strong>
  2.1</strong> – As inscri&ccedil;&otilde;es do Anime Quiz come&ccedil;am &agrave;s 10h00 do Sabado e ser&atilde;o finalizadas com meia hora de anteced&ecirc;ncia do concurso no palco.
  <br />
    <strong>2.2</strong> – Ser&atilde;o apenas 08 (oito) vagas para as equipes. Os trios que se inscreverem ap&oacute;s preenchimento de todas as vagas, ficar&atilde;o em lista de espera, caso o grupo n&atilde;o compare&ccedil;a no momento do campeonato.
    <br />
    <strong>2.3</strong> – A equipe tem o direito de inscrever 1 (um) substituto, que s&oacute; participar&aacute; do campeonato na falta de algum membro. O substituto n&atilde;o pode ser modificado ap&oacute;s a inscri&ccedil;&atilde;o, e n&atilde;o poder&aacute; estar inscrito em nenhum outro grupo.
    <br />
    <strong>2.4</strong> – No ato da inscri&ccedil;&atilde;o o trio deve estar presente para preencher as fichas de inscri&ccedil;&atilde;o.
    <br />
    <strong>2.5</strong> – Cada equipe deve escolher um nome, que n&atilde;o pode ser ofensivo, nem conter palavr&otilde;es ou palavras inapropriadas.
    <br />
    <strong>2.6</strong> – A inscri&ccedil;&atilde;o do Anime Quiz &eacute; gratuita.
    <br />
    <strong>2.7</strong> – A equipe deve ser composta por no m&aacute;ximo tr&ecirc;s componentes, contudo, &eacute; poss&iacute;vel se inscrever no Anime Quis em dupla ou individualmente.</div>
</p>
<strong> 3. </strong>Da Prepara&ccedil;&atilde;o
<br />
<div style="padding-left:20px;">
<strong>3.1</strong> – Os trios devem comparecer na sala do evento com meia hora de anteced&ecirc;ncia do concurso, para as &uacute;ltimas instru&ccedil;&otilde;es. O atraso acarretar&aacute; em desclassifica&ccedil;&atilde;o.
  <br />
    <strong>3.2</strong> – &Eacute; necess&aacute;rio que a equipe esteja presente, caso um n&atilde;o esteja presente, pode-se solicitar o substituto que foi previamente inscrito. Caso o substituto tamb&eacute;m n&atilde;o esteja presente, o participante concorrer&aacute; sozinho no Quiz.</div>
<strong>
</p>
4.</strong> Da Rodada de Perguntas <br />
<div style="padding-left:20px;">
<strong>4.1</strong> – Cada equipe enfrentar&aacute; outra equipe em uma rodada de perguntas, podendo conter questionamentos com ou sem alternativas, nome do personagem atrav&eacute;s de foto ou qualquer tipo de pergunta em rela&ccedil;&atilde;o ao anime.
  <br />
    <strong>4.2</strong> – Cada rodada conter&aacute; at&eacute; tr&ecirc;s turnos, sendo que no primeiro turno ser&aacute; exibida uma lista contendo tr&ecirc;s animes aleat&oacute;rios para cada equipe, o qual o trio escolher&aacute; um destes para responder tr&ecirc;s perguntas, no segundo turno ser&aacute; exibido novamente outra lista contendo tr&ecirc;s animes aleat&oacute;rios onde escolheram o anime para a equipe advers&aacute;ria responder. Caso haja empate, no terceiro turno, o julgador far&aacute; perguntas aleat&oacute;rias de qualquer anime para cada equipe, at&eacute; que haja um vencedor.
    <br />
    <strong>4.3</strong> – As chaves ser&atilde;o formadas aleatoriamente ap&oacute;s a inscri&ccedil;&atilde;o das duplas.</div>
    <strong>
</p>
5.</strong> Proibi&ccedil;&otilde;es
<br />
<div style="padding-left:20px;">
<strong>5.1</strong> – N&atilde;o &eacute; permitido uso de celular no momento do campeonato, nem de nenhum aparelho eletr&ocirc;nico.
  <br />
    <strong>5.2 </strong>– A equipe que receber ajuda da plat&eacute;ia ser&aacute; penalizada em uma quest&atilde;o anulada para cada ajuda que receber.
    <br />
    <strong>5.3 </strong>– Uma equipe n&atilde;o deve atrapalhar outra, podendo ser penalizada em uma quest&atilde;o anulada por isso.</div>
