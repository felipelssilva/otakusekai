<p>Cantor brasileiro de Animesongs famosas como temas de abertura brasileiro de Hunter X Hunter e Os Cavaleiros do Zod&iacute;aco Hades Inferno.</p>
  
<p>- Em 1999, com 17 anos, Ricardo faz interc&acirc;mbio de um ano no Jap&atilde;o, na cidade de Utsunomia, estado de Tochigi. L&aacute;, aprende a l&iacute;ngua japonesa e tem o primeiro contato mais estreito com o universo dos anime songs, assistindo a shows de Hironobu Kageyama e a turn&ecirc; Super Hero Spirits.</p>
<p>- De volta ao Brasil, em 2000, come&ccedil;a a trabalhar como redator na editora Conrad. L&aacute;, participa das revistas Her&oacute;i, Pok&eacute;mon Club, Nintendo World, Play, entre outras. Ao mesmo tempo, organiza em S&atilde;o Paulo pequenos eventos e encontros relacionados ah cultura pop japonesa.  </p>
<p>- Em 2003 participa da idealiza&ccedil;&atilde;o e organiza&ccedil;&atilde;o da primeira edi&ccedil;&atilde;o do evento Anime Friends.
    
  - Um ano depois, envia uma grava&ccedil;&atilde;o demo para Hironobu Kageyama, que o coloca no concurso que elegeria um novo membro para o grupo JAM Project.  </p>
<p>- Em 2005, Ricardo vence o concurso e vai para o Jap&atilde;o gravar seu primeiro single com o JAM, Gong. Desde ent&atilde;o, se torna membro especial do grupo, participando todos os anos das turn&ecirc;s pelo Jap&atilde;o e das grava&ccedil;&otilde;es dos CDs e DVDs do grupo.  </p>
<p>- Com a estreia profissional no JAM, come&ccedil;a a fazer muitos shows por todo o Brasil e Am&eacute;rica Latina.
    
  - Em paralelo, continua com o trabalho de jornalista free-lancer. Editou durante dois anos as revistas SAX (publica&ccedil;&atilde;o bimestral sobre cultura) e Mixmag Brasil (vers&atilde;o brasileira da maior revista de m&uacute;sica eletr&ocirc;nica da Inglaterra).  </p>
