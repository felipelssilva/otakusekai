<p style="text-align: justify;" id="Introducao">
			<strong>INTRODU&Ccedil;&Atilde;O</strong></p>
		<p style="text-align: justify;">
			O campeonato de cosplay sekai &eacute; um evento de fantasias relacionado ao mundo da anima&ccedil;&atilde;o que engloba historias em quadrinhos, desenhos, s&eacute;ries, seriados, filmes, games, etc. Que vem da cultura de v&aacute;rios pa&iacute;ses diferentes tendo como destaque o JAPAO famoso por seus animes e tokusatsus e os E.U.A famoso por seus HQs e o mundo do Cinema.&nbsp;</p>
<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;" id="Participacao">
			<strong>PARTICIPA&Ccedil;&Atilde;O</strong></p>
		<ul style="margin-left: 40px;">
			<li style="text-align: justify;">
		  Poder&aacute; participar do concurso qualquer pessoa n&atilde;o importando o sexo, idade ou origem.</li>
			<li style="text-align: justify;">
				Aos menores de 12 anos, a inscri&ccedil;&atilde;o dever&aacute; ser acompanhada por um adulto respons&aacute;vel.</li>
			<li style="text-align: justify;">
				A participa&ccedil;&atilde;o esta vedada apenas aos assistentes, coordenadores e ju&iacute;zes do evento.</li>
			<li style="text-align: justify;">
				O participante devera levar seu pr&oacute;prio &aacute;udio para as apresenta&ccedil;&otilde;es nas modalidades (COSPOBRE, GRUPO e INDIVIDUAL LIVRE), gravados em PENDRIVE somente.</li>
			<li style="text-align: justify;">
				A modalidade COSPLAY DESFILE ter&aacute; &aacute;udio pr&oacute;prio escolhido exclusivamente pela organiza&ccedil;&atilde;o do evento.</li>
</ul>
<p style="text-align: justify;">&nbsp;
</p>
		<p style="text-align: justify;" id="Inscricoes">
			<strong>INSCRI&Ccedil;&Otilde;ES</strong></p>
		<ul style="margin-left: 40px;">
			<li style="text-align: justify;">
		  Uma &uacute;nica pessoa poder&aacute; participar em todas as modalidades n&atilde;o importando se for utilizado o mesmo COSPLAY.</li>
			<li style="text-align: justify;">
				<span style="color:#ff0000;">S&oacute; ser&atilde;o permitidos COSPLAYERS que representem personagens de HQs, MANG&Aacute;S, Animes, Seriados, S&eacute;ries, Filmes, e Games em geral.</span></li>
			<li style="text-align: justify;">
				No dia do evento para se inscrever os COSPLAYERS dever&atilde;o comparecer ao respons&aacute;vel pelas inscri&ccedil;&otilde;es que estar&aacute; localizado no local indicado pelos organizadores.</li>
			<li style="text-align: justify;">
				Os materiais que dever&atilde;o ser levados e informados no ato da inscri&ccedil;&atilde;o ser&atilde;o: (Documento de Identidade (RG ou CPF), Idade, Nome, Endere&ccedil;o, Cidade, Estado, Telefone).</li>
			<li style="text-align: justify;">
				Ser&atilde;o aceitas inscri&ccedil;&otilde;es para o concurso de Cosplay com at&eacute; meia hora (30 minutos) de anteced&ecirc;ncia &aacute;s apresenta&ccedil;&otilde;es.</li>
			<li style="text-align: justify;">
				As inscri&ccedil;&otilde;es ter&atilde;o um limite m&iacute;nimo e m&aacute;ximo por categoria, assim sendo:</li>
</ul>
