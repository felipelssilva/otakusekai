</p>
		<p style="text-align: justify;" id="Categorias">
			<strong>CATEGORIAS</strong></p>
		<p style="text-align: justify;">
O concurso &eacute; dividido em quatro categorias: (Cospobre/ Cosplay Desfile/ Cosplay Grupo/ Cosplay Livre).</p>
		<p style="text-align: justify;">
			<strong>COSPOBRE </strong>= Os participantes desta modalidade dever&atilde;o se caracterizar utilizando materiais de baixo custo. Limite de participantes permitido por apresenta&ccedil;&atilde;o ser&aacute; de UM a CINCO indiv&iacute;duos.&nbsp;</p>
		<p style="text-align: justify;">
			(O tempo m&aacute;ximo permitido por apresenta&ccedil;&atilde;o ser&aacute; de 2min.)</p>
		<p style="text-align: justify;">
			<strong>COSPLAY DESFILE</strong> = Nesta modalidade os participantes dever&atilde;o desfilar no palco onde poder&atilde;o utilizar de falas ou posi&ccedil;&otilde;es dos personagens representados.</p>
		<p style="text-align: justify;">
			(O tempo m&aacute;ximo permitido por apresenta&ccedil;&atilde;o ser&aacute; de 30seg.)</p>
		<p style="text-align: justify;">
			<strong>COSPLAY INDIVIDUAL/DUPLA</strong> = Aqui os participantes dever&atilde;o escolher entre participar individualmente ou em duplas, onde aos participantes que forem individualmente poder&atilde;o optar por utilizar um ajudante de palco.</p>
		<p style="text-align: justify;">
			(O tempo m&aacute;ximo permitido ser&aacute; de 2min por apresenta&ccedil;&atilde;o.)</p>
		<p style="text-align: justify;">
			<strong>COSPLAY GRUPO </strong>= Nesta modalidade os participantes dever&atilde;o se inscrever em grupos de quatro ou mais indiv&iacute;duos podendo se utilizar de quantos ajudantes de palco necessitar. O limite m&aacute;ximo para participantes ser&aacute; de 12 pessoas por grupo incluindo assistentes de palco.</p>
		<p style="text-align: justify;">
			(O tempo Maximo permitido ser&aacute; de 3min por apresenta&ccedil;&atilde;o.)</p>
<p style="text-align: center;">
			<span style="color:#ff0000;">(O tempo limite poder&aacute; ser alterado segundo o crit&eacute;rio dos avaliadores.)</span></p>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;" id="Regras_e_Apresentacao"><strong>REGRAS e APRESENTA&Ccedil;&Atilde;O		</strong></p>
		<ul style="margin-left: 40px;">
		  <li style="text-align: justify;">
		    As apresenta&ccedil;&otilde;es ser&atilde;o em hor&aacute;rio definidos pela organiza&ccedil;&atilde;o do evento OTAKU SEKAI.</li>
		  <li style="text-align: justify;">
		    Os COSPLAYERS dever&atilde;o comparecer preparados no local de apresenta&ccedil;&atilde;o com TRINTA MINUTOS de anteced&ecirc;ncia do hor&aacute;rio estabelecido, pois ter&atilde;o que ser avaliados no quesito FANTASIA antes da subida ao palco.</li>
		  <li style="text-align: justify;">
		    Os COSPLAYERS dever&atilde;o ficar atentos a ordem de chamada para as apresenta&ccedil;&otilde;es, onde n&atilde;o respeitando esta regra estar&aacute; sujeito a penalidade ou desclassifica&ccedil;&atilde;o.</li>
		  <li style="text-align: justify;">
		    Durante sua apresenta&ccedil;&atilde;o o COSPLAYER &eacute; inteiramente respons&aacute;vel pelo que acontecer no palco.</li>
		  <li style="text-align: justify;">
		    Quaisquer danos ocasionados no equipamento ou em qualquer equipamento sendo pr&oacute;prio ou do evento s&atilde;o de responsabilidade sua.</li>
		  <li style="text-align: justify;">
		    &Eacute; permitido ao COSPLAYER dirigir a palavra aos espectadores como um todo, desde que isso n&atilde;o seja em linguajar ofensivo ou que contem palavras de baixo escal&atilde;o. E proibido tamb&eacute;m falar mal, caluniar, insultar ou ca&ccedil;oar qualquer pessoa, entidade, empresa ou clube durante a apresenta&ccedil;&atilde;o.</li>
		  <li style="text-align: justify;">
		    E proibido atirar objetos ou qualquer material para ou contra os espectadores e/ou ju&iacute;zes. O COSPLAYER poder&aacute; usar esses recursos limitando-se ao espa&ccedil;o do palco.</li>
		  <li style="text-align: justify;">
		    E permitida &agrave; montagem de cen&aacute;rios para apresenta&ccedil;&atilde;o da modalidade COSPOBRE/ INDIVIDUAL e GRUPO, porem o competidor ter&aacute; o limite de 2min para montagem.</li>
		  <li style="text-align: justify;">
		    &Eacute; permitida a utiliza&ccedil;&atilde;o de replicas de armas de madeira, papel, resina, pl&aacute;stico de feito artesanal. N&atilde;o e permitida o uso de replicas de armas de metais e vidro.</li>
		  <li style="text-align: justify;">
		    &Eacute; proibida a utiliza&ccedil;&atilde;o de materiais inflam&aacute;veis, pontiagudos, de corte, ou qualquer material que coloque em risco a integridade f&iacute;sica do usu&aacute;rio. Todo material utilizado na apresenta&ccedil;&atilde;o devera ser averiguado pelo staff respons&aacute;vel antes das apresenta&ccedil;&otilde;es.</li>
		  <li style="text-align: justify;">
		    E proibida &agrave; divulga&ccedil;&atilde;o de sites, empresas e produtos.&nbsp;</li>
</ul>
		<p style="text-align: center;">
			<span style="font-size:20px;"><span style="color:#ff0000;"><strong>&ldquo;Aten&ccedil;&atilde;o! Para n&atilde;o ser surpreendido na portaria com a apreens&atilde;o de componente de seu Cosplay fique atento as normas gerais do evento!&rdquo;</strong></span></span></p>
<p style="text-align: center;">&nbsp;
</p>
		<p style="text-align: justify;" id="Desclassificacao">
			<strong>DESCLASSIFICA&Ccedil;&Atilde;O</strong></p>
		<ul>
			<li style="text-align: justify;">
		  O n&atilde;o cumprimento das regras acima acarretara na desclassifica&ccedil;&atilde;o do participante.</li>
			<li style="text-align: justify;">
				O n&atilde;o preenchimento da ficha de inscri&ccedil;&atilde;o ou preenchimento incorreto acarretara na desclassifica&ccedil;&atilde;o do participante.</li>
</ul>
