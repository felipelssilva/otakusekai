<h1>BANDAS</h1>


<strong><p>The Kira Justice:</p>
<p><img src="Fotos/TheKiraJustice.png" alt="" width="600" height="224"></p>
</strong>

<p>A banda The Kira Justice surgiu no ano de 2007, em Porto Alegre, Rio Grande do Sul, e em pouco tempo de estrada ganhou os palcos de v&aacute;rios festivais de anime, tendo feito shows em dezenas de estados Brasileiros em todas as regi&otilde;es do pa&iacute;s e inclusive uma turn&ecirc; pela Argentina em 2012.</p>
<p>O repert&oacute;rio da banda traz desde temas populares dos anos 90 e 00 como Pok&eacute;mon, Cavaleiros do Zod&iacute;aco, Digimon, e Dragon Ball at&eacute; animes mais “atuais” como Naruto, One Piece e Fairy Tail. A banda ainda traz can&ccedil;&otilde;es autorais, inspiradas tamb&eacute;m no universo da anima&ccedil;&atilde;o japonesa, completando o set com m&uacute;sicas para tirar o p&eacute; do ch&atilde;o.</p>
<p>A banda liderada pelos irm&atilde;os Matheus (guitarra/voz) e Sarisa (voz), tem sido uma revela&ccedil;&atilde;o em diferentes m&iacute;dias: na internet eles t&ecirc;m mais de 3 milh&otilde;es e meio de visualiza&ccedil;&otilde;es s&oacute; no Youtube, al&eacute;m de dezenas de milhares de f&atilde;s em redes sociais como Twitter e Facebook, sendo considerada a mais popular do Brasil no g&ecirc;nero, al&eacute;m de ser considerada a melhor do segmento no pa&iacute;s por premia&ccedil;&otilde;es especializadas. Fora da internet a banda foi alvo mat&eacute;rias de TVs, r&aacute;dios, jornais e revistas por dezenas de estados, inclusive de circula&ccedil;&atilde;o nacional.</p>
