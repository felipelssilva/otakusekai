				<li style="text-align: justify;">
					COSPLAY: O acabamento, similaridade aos trajes originais do personagem utilizando de materiais recicl&aacute;veis e ou baratos, riqueza de detalhes de acess&oacute;rios e detalhes.</li>
				<li style="text-align: justify;">
