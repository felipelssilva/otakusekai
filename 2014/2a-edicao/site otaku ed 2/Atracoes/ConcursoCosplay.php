			<ul style="margin-left: 40px;">
				<li style="text-align: justify;">
					As apresenta&ccedil;&otilde;es ser&atilde;o em hor&aacute;rio definidos pela organiza&ccedil;&atilde;o do Evento OTAKU SEKAI.</li>
				<li style="text-align: justify;">
					Os COSPLAYERS dever&atilde;o comparecer preparados no local de apresenta&ccedil;&atilde;o com TRINTA MINUTOS de anteced&ecirc;ncia do hor&aacute;rio estabelecido para as apresenta&ccedil;&otilde;es, pois ter&atilde;o que ser avaliados no quesito FANTASIA antes da subida ao palco.</li>
				<li style="text-align: justify;">
					Os COSPLAYERS dever&atilde;o ficar atentos a ordem de chamada para as apresenta&ccedil;&otilde;es, onde n&atilde;o respeitando esta regra estar&aacute; sujeito a penalidade ou desclassifica&ccedil;&atilde;o.</li>
				<li style="text-align: justify;">
					Durante sua apresenta&ccedil;&atilde;o o COSPLAYER &eacute; inteiramente respons&aacute;vel pelo que acontecer no palco durante sua apresenta&ccedil;&atilde;o.</li>
				<li style="text-align: justify;">
					Quaisquer danos ocasionados no equipamento ou em qualquer equipamento sendo pr&oacute;prio ou do evento s&atilde;o de responsabilidade sua. Salvo em determinadas ocasi&otilde;es.</li>
				<li style="text-align: justify;">
					&Eacute; permitido ao COSPLAYER dirigir a palavra aos espectadores como um todo, desde que isso n&atilde;o seja em linguajar ofensivo ou que contem palavras de baixo escal&atilde;o. E proibido tamb&eacute;m falar mal, caluniar, insultar ou ca&ccedil;oar qualquer pessoa, entidade, empresa ou clube durante a apresenta&ccedil;&atilde;o.</li>
				<li style="text-align: justify;">
					E proibido atirar objetos ou qualquer material para ou contra os espectadores e/ou ju&iacute;zes. O COSPLAYER poder&aacute; usar esses recursos limitando-se ao espa&ccedil;o do palco.</li>
				<li style="text-align: justify;">
					E permitida &agrave; montagem de cen&aacute;rios para apresenta&ccedil;&atilde;o da modalidade COSPOBRE/ INDIVIDUAL e GRUPO, porem o competidor ter&aacute; o limite de 2min para montagem.</li>
				<li style="text-align: justify;">
					&Eacute; permitida a utiliza&ccedil;&atilde;o de replicas de armas de madeira, papel, resina, pl&aacute;stico. N&atilde;o e permitida o uso de replicas de armas de metais e vidro.</li>
				<li style="text-align: justify;">
					&Eacute; proibida a utiliza&ccedil;&atilde;o de materiais inflam&aacute;veis, pontiagudos, de corte, ou qualquer material que coloque em risco a integridade f&iacute;sica do usu&aacute;rio. Todo material utilizado na apresenta&ccedil;&atilde;o devera ser averiguado pelo staff respons&aacute;vel antes das apresenta&ccedil;&otilde;es.</li>
				<li style="text-align: justify;">
					E proibida &agrave; divulga&ccedil;&atilde;o de sites, empresas e produtos.</li>
			</ul>
